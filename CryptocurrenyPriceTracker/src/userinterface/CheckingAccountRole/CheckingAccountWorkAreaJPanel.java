    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCheckingAccountRequest.getSelectedRow();
        if (tblCheckingAccountRequest.getValueAt(selectedRow, 5).toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please assign the selected request.");
            return;

        }
        

        else if ((selectedRow >= 0) && !(tblCheckingAccountRequest.getValueAt(selectedRow, 5)).toString().isEmpty()) {
            CheckingAccountWorkRequest request = (CheckingAccountWorkRequest) tblCheckingAccountRequest.getValueAt(selectedRow, 0);

            request.setStatus("Processing");

            ProcessCheckingAccountRequestJPanel panel = new ProcessCheckingAccountRequestJPanel(userProcessContainer, request,ecosystem);
            userProcessContainer.add("ProcessAdviceJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
            System.out.println("Request selected for processing of checking account ");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
            return;
        }
        
    }//GEN-LAST:event_btnProcessActionPerformed

