    public void populateTable(){
    DefaultTableModel model = (DefaultTableModel) tblCheckingAccountRequest.getModel();
        
  
        Organization found = null;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof CheckingAccountOrganization) {
                found = o;
                break;
            }
        }
    
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        model.setRowCount(0);
        for (WorkRequest wr : found.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof CheckingAccountWorkRequest && (wr.getReceiver() == null || wr.getReceiver() == account)) {
                CheckingAccountWorkRequest awr = (CheckingAccountWorkRequest) wr;
                Object[] row = new Object[8];
                row[0] = awr;
                row[1]=sdf.format(awr.getRequestDate());
                row[2]=awr.getsNetwork();
                row[3]=awr.getsEnterprise();
                row[4] = awr.getsMessage();
                UserAccount r = awr.getReceiver();
                row[5] = r != null ? r : "";
                row[6]=awr.getStatus();
                row[7] = awr.getSender().getUsername();
                
          
                
                //row[5]=awr.getResolveDate() != null ?  sdf.format(awr.getResolveDate()): "";
                
                model.addRow(row);
            }
        }
    
    }
    

