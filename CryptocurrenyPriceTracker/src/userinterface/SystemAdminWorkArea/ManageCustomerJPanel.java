    private void btnViewCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewCustomerActionPerformed
        // TODO add your handling code here:
        System.out.println("Activity :view customers");
        int selectedRow = tblCustomer.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row from table to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Customer customer = (Customer) tblCustomer.getValueAt(selectedRow, 0);
            ViewCustomerJPanel viewCustomerJPanel = new ViewCustomerJPanel(userProcessContainer, customer, customerDirectory, account);
            userProcessContainer.add("viewCustomerJPanel", viewCustomerJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewCustomerActionPerformed

