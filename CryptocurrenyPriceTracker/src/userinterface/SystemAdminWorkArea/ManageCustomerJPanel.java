    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);

        for (Customer c : customerDirectory.getCustomerList()) {
            Object row[] = new Object[4];
            row[0] = c;
            row[1] = c.getsFirstName();
            row[2] = c.getsLastName();
           row[3] = c.getsEmailId();
            dtm.addRow(row);
        }
    }
