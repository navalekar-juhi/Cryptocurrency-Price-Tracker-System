    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAdviceRequest.getSelectedRow();
        if (selectedRow >= 0) {
        WorkRequest wr = (WorkRequest) tblAdviceRequest.getValueAt(selectedRow, 0);
        ViewAdviceJPanel panel = new ViewAdviceJPanel(userProcessContainer, (AdviceWorkRequest) wr,ecosystem);
        userProcessContainer.add("ViewAdviceJPanel", panel);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a request to see the details.");
            return;
        }
        System.out.println("View the suggested details sent by advisory");
    }//GEN-LAST:event_btnDetailsActionPerformed


