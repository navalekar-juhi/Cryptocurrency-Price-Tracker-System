    public void populatetable(){
    DefaultTableModel model = (DefaultTableModel) tblAdviceRequest.getModel();
        
        model.setRowCount(0);
        Customer c = (Customer) ecosystem.getCustomerDirectory().searchCustomer(account.getUsername());
        for (WorkRequest request :account.getWorkQueue().getWorkRequestList()){
            if (request instanceof AdviceWorkRequest){
            Object[] row = new Object[7];
            row[0] = ((AdviceWorkRequest) request);
            row[1] = request.getRequestDate();
            row[2]= ((AdviceWorkRequest) request).getsNetwork();
            row[3]= ((AdviceWorkRequest) request).getsEnterprise();
            row[4]= ((AdviceWorkRequest) request).getdAmount();
            row[5] = request.getReceiver();
            row[6] = request.getStatus();
            
            model.addRow(row);
            }
        }
    
    }
